package com.bridgelabz.basics;

public class PrimitiveDataDefault {
    static int val1;
    static long val2;
    static float val3;
    static double val4;
    static String val5;
    static boolean val6;

    public static void main(String[] args) {
        System.out.println("printing default data for primitive data type ");
        System.out.println("value for int: "+val1);
        System.out.println("value for long: "+val2);
        System.out.println("value for float: "+val3);
        System.out.println("value for double: "+val4);
        System.out.println("value for String: "+val5);
        System.out.println("value for boolean: "+val6);

    }
}
